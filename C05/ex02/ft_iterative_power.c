int	ft_iterative_power(int nb, int power)
{
	int	i;
	int	k;

	k = nb;
	i = 1;
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	while (i < power)
	{
		nb *= k;
		i++;
	}
	return (nb);
}
